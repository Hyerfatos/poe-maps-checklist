function generateList(tier, family) {
    beast = Beasts[family]
    var buffer = "";
    var counter = 0;
    $.each(beast, function(group, valGroup){ // Loop through Group
        var tmp = group.replace(" ", "_");
        buffer += '<h3 id="The_' + tmp +'"><a href="#The_' + tmp + '_col" data-toggle="collapse" class="btn btn-primary btn-collapse btn-sm"></a>' + group + '</h3>'
        buffer += '<ul id="The_' + tmp + '_col" class="collapse in">'
    
        $.each(valGroup, function(genus, valGenus){
            var tmp = genus.replace(" ", "_");
            buffer += '<h3 id="The_' + tmp +'"><a href="#The_' + tmp + '_col" data-toggle="collapse" class="btn btn-primary btn-collapse btn-sm"></a>' + genus + '</h3>'
            buffer += '<ul id="The_' + tmp + '_col" class="collapse in">'
            
            $.each(valGenus, function(index, val){
                counter++;
                buffer += '<li data-id="beastlist_' + tier + '_' + counter + '"><a href=' + val["url"] + '>' + val["name"] + '</a></li>'
            });
            
            buffer += '</ul>'
        });
        buffer += '</ul>'     
    });
    return buffer;
}

